function fotocopyOpen() {
    var fotocopy_buka = document.getElementById("fotocopy_buka").value;

    if(fotocopy_buka == "fotocopy"){
        document.getElementById("fotocopy_detail").style.display = "block";
    }
}

function fotocopyClose() {
    var fotocopy_tutup = document.getElementById("fotocopy_tutup").value;

    if(fotocopy_tutup == "tutup"){
        document.getElementById("fotocopy_detail").style.display = "none";
    }
}

